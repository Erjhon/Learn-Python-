# Pyramid in python
pyramid = int(input("Enter number of rows: "))

for i in range(pyramid):
    for j in range(i+1):
        print("* ", end="")
    print("\n")                 

# Square pattern in python
square = int(input("Enter side of a square: "))

for i in range(square):
    for i in range(square):
        print('*', end = '')
    print("\n")




    